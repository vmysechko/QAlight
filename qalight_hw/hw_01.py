# String operations
text01 = "It`s"
text02 = "Friday!"
text03 = text01 + " " + text02
print(text03, "\n")

# Integer operations
int_num01 = 20
int_num02 = 10
print("Int Sum: ", int_num01 + int_num02)
print("Int Difference: ", int_num01 - int_num02)
print("Int Multiplication: ", int_num01 * int_num02)
print("Int Division: ", int_num01 / int_num02,  "\n")

# Float operations
float_num01 = 25.5
float_num02 = 13.3
print("Float Sum: ", float_num01 + float_num02)
print("Float Difference: ", float_num01 - float_num02)
print("Float Multiplication: ", round(float_num01 * float_num02, 1))
print("Float Division: ", round(float_num01 / float_num02, 1))


